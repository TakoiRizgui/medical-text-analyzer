import streamlit as st
import json
import io
from datetime import datetime
from fpdf import FPDF
from extractor import analyze_text, PARAMETERS

def clean_for_pdf(text: str) -> str:
    return text.encode('latin-1', errors='replace').decode('latin-1').strip()

# -----------------------------------------------
# Load translations
# -----------------------------------------------

def load_translations(lang: str) -> dict:
    with open(f"locales/{lang}.json", encoding="utf-8") as f:
        return json.load(f)

# -----------------------------------------------
# PDF Export
# -----------------------------------------------

def generate_pdf(findings: list, user_input: str, tr: dict, lang: str) -> bytes:
    s = clean_for_pdf  # alias for brevity

    pdf = FPDF()
    pdf.add_page()
    pdf.set_auto_page_break(auto=True, margin=15)

    pdf.set_font("Helvetica", "B", 16)
    pdf.set_fill_color(240, 248, 255)
    pdf.cell(0, 12, "Clinical Lab Results Report", new_x="LMARGIN", new_y="NEXT", align="C", fill=True)
    pdf.set_font("Helvetica", size=9)
    pdf.cell(0, 6, f"Generated: {datetime.now().strftime('%Y-%m-%d %H:%M')}", new_x="LMARGIN", new_y="NEXT", align="C")
    pdf.ln(4)

    pdf.set_font("Helvetica", "I", 8)
    pdf.set_text_color(150, 0, 0)
    pdf.multi_cell(0, 5, "DISCLAIMER: Educational purposes only. Not medical advice.")
    pdf.set_text_color(0, 0, 0)
    pdf.ln(4)

    pdf.set_font("Helvetica", "B", 11)
    pdf.cell(0, 8, "Input:", new_x="LMARGIN", new_y="NEXT")
    pdf.set_font("Helvetica", size=9)
    pdf.set_fill_color(248, 248, 248)
    pdf.multi_cell(0, 5, s(user_input[:300]), fill=True)
    pdf.ln(4)

    has_abnormal = any(f["status_key"] != "normal" for f in findings)
    pdf.set_font("Helvetica", "B", 12)
    if has_abnormal:
        pdf.set_text_color(180, 0, 0)
        pdf.cell(0, 8, "Result: Out-of-range values detected", new_x="LMARGIN", new_y="NEXT")
    else:
        pdf.set_text_color(0, 120, 0)
        pdf.cell(0, 8, "Result: All values within normal range", new_x="LMARGIN", new_y="NEXT")
    pdf.set_text_color(0, 0, 0)
    pdf.ln(3)

    pdf.set_font("Helvetica", "B", 10)
    pdf.set_fill_color(220, 235, 255)
    col_w = [45, 25, 15, 55, 50]
    for h in ["Parameter", "Value", "Unit", "Status", "Reference"]:
        pdf.cell(col_w[["Parameter","Value","Unit","Status","Reference"].index(h)], 7, h, border=1, fill=True)
    pdf.ln()

    pdf.set_font("Helvetica", size=8)
    for f in findings:
        if f["status_key"] != "normal":
            pdf.set_fill_color(255, 235, 235)
        else:
            pdf.set_fill_color(235, 255, 235)
        op = f.get("operator") or ""
        pdf.cell(col_w[0], 6, s(f["name"][:22]), border=1, fill=True)
        pdf.cell(col_w[1], 6, s(f"{op}{f['value']}"), border=1, fill=True)
        pdf.cell(col_w[2], 6, s(f["unit"][:10]), border=1, fill=True)
        pdf.cell(col_w[3], 6, s(f["status"][:30]), border=1, fill=True)
        pdf.cell(col_w[4], 6, s(f["reference"][:48]), border=1, fill=True)
        pdf.ln()

    abnormal = [f for f in findings if f["status_key"] != "normal" and f["advice"]]
    if abnormal:
        pdf.ln(5)
        pdf.set_font("Helvetica", "B", 11)
        pdf.cell(0, 8, "Recommendations:", new_x="LMARGIN", new_y="NEXT")
        for f in abnormal:
            pdf.set_font("Helvetica", "B", 9)
            pdf.cell(0, 6, s(f"  {f['name']}:"), new_x="LMARGIN", new_y="NEXT")
            pdf.set_font("Helvetica", size=8)
            pdf.multi_cell(0, 5, s(f"  {f['advice']}"))
            pdf.ln(2)

    pdf.ln(5)
    pdf.set_font("Helvetica", "I", 7)
    pdf.set_text_color(120, 120, 120)
    pdf.cell(0, 5, "Generated by Clinical Lab Results Interpreter - educational use only.", new_x="LMARGIN", new_y="NEXT", align="C")

    return bytes(pdf.output())

# -----------------------------------------------
# UI Setup
# -----------------------------------------------

st.set_page_config(page_title="Clinical Lab Results Interpreter", layout="centered")

SAMPLES = {
    "en": "Ferritin 8 ng/mL, Hemoglobin 10.2 g/dL, Glucose 140 mg/dL, CRP 45 mg/L, TSH 0.2 mIU/L",
    "fr": "Ferritine 8 ng/mL, Hémoglobine 10.2 g/dL, Glycémie 140 mg/dL, CRP 45 mg/L, TSH 0.2 mIU/L",
    "ar": "Ferritin 8 ng/mL, Hemoglobin 10.2 g/dL, Glucose 140 mg/dL, CRP 45 mg/L, TSH 0.2 mIU/L",
}

# Language selector
with st.sidebar:
    lang = st.selectbox(
        "🌐 Language",
        ["en", "fr", "ar"],
        format_func=lambda x: {"en": "🇬🇧 English", "fr": "🇫🇷 Français", "ar": "🇸🇦 العربية"}[x]
    )

tr = load_translations(lang)

# RTL for Arabic
if lang == "ar":
    st.markdown("""<style>
        .stApp, .stTextArea textarea, p, label { direction: rtl; text-align: right; }
    </style>""", unsafe_allow_html=True)

# Sidebar content
with st.sidebar:
    st.markdown("---")
    st.caption(f"📚 {len(PARAMETERS)} {tr['sidebar_params']}")
    with st.expander(tr["sidebar_how"]):
        st.markdown(tr["sidebar_how_text"])

# Main UI
st.title(tr["title"])
st.write(tr["subtitle"])
st.markdown(tr["disclaimer"])

col1, col2 = st.columns([3, 1])
with col1:
    st.markdown(f"**{tr['input_label']}**")
with col2:
    if st.button(tr["sample_btn"]):
        st.session_state["sample_text"] = SAMPLES[lang]

default_text = st.session_state.get("sample_text", "")
user_input = st.text_area(
    "", value=default_text, height=130,
    placeholder=tr["placeholder"],
    label_visibility="collapsed"
)

# -----------------------------------------------
# Analysis
# -----------------------------------------------

if st.button(tr["button"], type="primary"):
    if user_input.strip() == "":
        st.warning(tr["empty_warning"])
    else:
        findings = analyze_text(user_input, lang)

        if not findings:
            st.warning(tr["no_interp"])
        else:
            has_abnormal = any(f["status_key"] != "normal" for f in findings)
            if has_abnormal:
                st.error(tr["result_abnormal"])
            else:
                st.success(tr["result_normal"])

            # Detailed results
            st.subheader(tr["interp_title"])
            for f in findings:
                icon = "✅" if f["status_key"] == "normal" else "⚠️"
                op = f.get("operator") or ""
                with st.expander(f"{icon} {f['name']} — {op}{f['value']} {f['unit']}"):
                    st.markdown(f"**{f['status']}**")
                    st.caption(f"📐 {tr['ref_label']}: {f['reference']}")
                    if f["advice"]:
                        st.info(f"💡 {f['advice']}")

            # PDF Export
            st.markdown("---")
            pdf_bytes = generate_pdf(findings, user_input, tr, lang)
            st.download_button(
                label=tr["export_btn"],
                data=pdf_bytes,
                file_name=tr["export_filename"],
                mime="application/pdf"
            )
